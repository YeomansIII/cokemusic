var Room = Class.extend({
	/**
	 * Gets the width of the entire room.
	 * the width is set by loading in the type.
	 **/
	width : function() {
		return this._width;
	},

	/**
	 * Gets the height of the entire room.
	 * the height is set by loading in the type.
	 **/
	height : function() {
		return this._height;
	}
});
